def _security_env_scripts(keystore: Path, strategy: str) -> dict[str, str]:
    """Generate per-shell setup scripts that export security env vars."""
    env_vars = {
        "ROS_SECURITY_ENABLE":   "true",
        "ROS_SECURITY_STRATEGY": strategy,
        "ROS_SECURITY_KEYSTORE": str(keystore),
    }

    bash_lines   = [f'export {k}="{v}"' for k, v in env_vars.items()]
    fish_lines   = [f"set -x {k} {v}"   for k, v in env_vars.items()]
    win_lines    = [f"set {k}={v}"       for k, v in env_vars.items()]
    ps_lines     = [f'$env:{k} = "{v}"' for k, v in env_vars.items()]

    note = (
        "# Source this file in every terminal before running ROS 2 nodes.\n"
        "# Each node must also have a keystore entry — see:\n"
        "#   ros2 security create_enclave <keystore> <enclave_path>\n"
    )

    return {
        "setup_security.bash": (
            "#!/usr/bin/env bash\n"
            + note
            + "\n".join(bash_lines)
            + "\necho '[ros2_network_fixer] SROS2 security environment set.'\n"
        ),
        "setup_security.fish": (
            "#!/usr/bin/env fish\n"
            + note.replace("#", "#")
            + "\n".join(fish_lines)
            + "\necho '[ros2_network_fixer] SROS2 security environment set.'\n"
        ),
        "setup_security.bat": (
            "@echo off\n"
            + "REM " + note.replace("\n", "\nREM ").rstrip("REM ")
            + "\n".join(win_lines)
            + "\necho [ros2_network_fixer] SROS2 security environment set.\n"
        ),
        "setup_security.ps1": (
            "# Source: . .\\setup_security.ps1\n"
            + note.replace("#", "#")
            + "\n".join(ps_lines)
            + "\nWrite-Host '[ros2_network_fixer] SROS2 security environment set.'\n"
        ),
    }


