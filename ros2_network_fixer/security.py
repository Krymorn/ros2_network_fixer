def _sros2_available() -> bool:
    """Return True if ros2 security subcommand is reachable."""
    rc, out, _ = _run(["ros2", "security", "--help"], timeout=6)
    return rc == 0


def _create_keystore(keystore: Path) -> bool:
    """Run `ros2 security create_keystore <path>`."""
    keystore.parent.mkdir(parents=True, exist_ok=True)
    rc, out, err = _run(["ros2", "security", "create_keystore", str(keystore)], timeout=30)
    if rc == 0:
        ui.ok(f"Keystore created: {keystore}")
        return True
    ui.error(f"ros2 security create_keystore failed (exit {rc})")
    if err:
        ui.detail(err)
    return False


def _create_enclave(keystore: Path, enclave: str) -> bool:
    """Run `ros2 security create_enclave <keystore> <enclave>`."""
    rc, out, err = _run(
        ["ros2", "security", "create_enclave", str(keystore), enclave],
        timeout=30,
    )
    if rc == 0:
        ui.ok(f"Enclave created: {enclave}")
        return True
    ui.error(f"ros2 security create_enclave failed for {enclave}")
    if err:
        ui.detail(err)
    return False


def _sign_governance(keystore: Path) -> bool:
    """
    Write governance.xml and sign it with the CA key.
    Returns True even if signing is skipped (unsigned .xml still useful
    for users who want to sign manually or use permissive mode).
    """
    gov_xml_path = keystore / "public" / "governance.xml"
    gov_xml_path.parent.mkdir(parents=True, exist_ok=True)
    gov_xml_path.write_text(_governance_xml())
    ui.ok(f"Governance XML written: {gov_xml_path}")

    # Try to sign via ros2 security sign_governance
    rc, _, err = _run(
        ["ros2", "security", "sign_governance",
         str(keystore), str(gov_xml_path)],
        timeout=15,
    )
    if rc == 0:
        ui.ok("Governance document signed (governance.p7s)")
        return True

    # Older distros use openssl directly
    ca_cert = keystore / "public" / "ca.cert.pem"
    ca_key  = keystore / "private" / "ca.key.pem"
    out_p7s = keystore / "public" / "governance.p7s"

    if ca_cert.exists() and ca_key.exists() and shutil.which("openssl"):
        rc2, _, err2 = _run([
            "openssl", "smime", "-sign",
            "-in",    str(gov_xml_path),
            "-text",
            "-out",   str(out_p7s),
            "-signer", str(ca_cert),
            "-inkey",  str(ca_key),
            "-noattr", "-nodetach",
        ], timeout=15)
        if rc2 == 0:
            ui.ok("Governance document signed via openssl (governance.p7s)")
            return True
        ui.warn("openssl signing failed — governance.xml written but not signed.")
        if err2:
            ui.detail(err2)
    else:
        ui.warn("Could not sign governance document automatically.")

    ui.info("Sign manually with:")
    ui.cmd_block(
        f"openssl smime -sign -in {gov_xml_path} -text "
        f"-out {keystore}/public/governance.p7s "
        f"-signer {keystore}/public/ca.cert.pem "
        f"-inkey {keystore}/private/ca.key.pem -noattr -nodetach"
    )
    return True  # Non-fatal — unsigned XML is still written


