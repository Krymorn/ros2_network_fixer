def check_security_posture(env: EnvironmentInfo) -> list:
    """
    Return a list of CheckResult-compatible dicts describing the current
    security posture.  Imported by diagnostics.py.
    """
    from .diagnostics import CheckResult

    results = []

    # 1. ROS_SECURITY_ENABLE
    sec_enable = os.environ.get("ROS_SECURITY_ENABLE", "").lower()
    if sec_enable == "true":
        results.append(CheckResult(
            name="ROS_SECURITY_ENABLE",
            passed=True,
            message="DDS security is enabled (ROS_SECURITY_ENABLE=true).",
        ))
    else:
        results.append(CheckResult(
            name="ROS_SECURITY_ENABLE",
            passed=False,
            message="DDS security is OFF — traffic is unencrypted and unauthenticated.",
            fix_hint="Run '--fix security' to set up SROS2 encryption and authentication.",
        ))

    # 2. ROS_SECURITY_STRATEGY
    strategy = os.environ.get("ROS_SECURITY_STRATEGY", "")
    if strategy in ("Enforce", "Permissive"):
        results.append(CheckResult(
            name="ROS_SECURITY_STRATEGY",
            passed=strategy == "Enforce",
            message=f"Security strategy: {strategy}. {STRATEGIES.get(strategy, '')}",
        ))
    elif sec_enable == "true":
        results.append(CheckResult(
            name="ROS_SECURITY_STRATEGY",
            passed=False,
            message="ROS_SECURITY_STRATEGY is not set (defaults to Permissive).",
            fix_hint="Set ROS_SECURITY_STRATEGY=Enforce to block unsigned nodes.",
        ))

    # 3. ROS_SECURITY_KEYSTORE
    keystore_path = os.environ.get("ROS_SECURITY_KEYSTORE", "")
    if keystore_path:
        ks = Path(keystore_path)
        if ks.exists():
            results.append(CheckResult(
                name="ROS_SECURITY_KEYSTORE",
                passed=True,
                message=f"Keystore found: {ks}",
            ))
        else:
            results.append(CheckResult(
                name="ROS_SECURITY_KEYSTORE",
                passed=False,
                message=f"Keystore path set but directory does not exist: {ks}",
                fix_hint="Run '--fix security' to create the keystore.",
            ))
    elif sec_enable == "true":
        results.append(CheckResult(
            name="ROS_SECURITY_KEYSTORE",
            passed=False,
            message="ROS_SECURITY_KEYSTORE is not set.",
            fix_hint="Run '--fix security' to configure the keystore path.",
        ))

    # 4. FASTRTPS_DEFAULT_PROFILES_FILE pointing at security profile
    profiles_file = os.environ.get("FASTRTPS_DEFAULT_PROFILES_FILE", "")
    security_xml = CONFIG_DIR / "fastdds_security.xml"
    if profiles_file and "security" in profiles_file.lower():
        results.append(CheckResult(
            name="Fast DDS security profile",
            passed=True,
            message=f"Security XML profile active: {profiles_file}",
        ))
    elif security_xml.exists():
        results.append(CheckResult(
            name="Fast DDS security profile",
            passed=False,
            message=f"Security XML profile exists but is not active.",
            fix_hint=f"Source setup_security.bash (or .ps1/.bat) from {CONFIG_DIR}",
        ))

    return results


