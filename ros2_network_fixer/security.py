"""
security.py — SROS2 and DDS-Security configuration for ROS 2.

Handles:
  - Detection of current security posture (env vars, keystore presence)
  - SROS2 keystore creation via `ros2 security` CLI
  - Fast DDS security XML profile generation (encryption + signing)
  - Per-node permission and governance document generation
  - Environment variable setup scripts for all shells
  - Security diagnostic checks

DDS-Security standard defines five security plugins:
  1. Authentication  — verifies node identity via X.509 certificates
  2. Access Control  — enforces topic publish/subscribe permissions
  3. Cryptographic   — encrypts RTPS messages (AES-128-GCM or AES-256-GCM)
  4. Logging         — optional audit trail
  5. Data Tagging    — optional data classification labels

SROS2 configures plugins 1-3 by default.  This module enables all three
and generates the minimal set of policy files needed to get a working
secure ROS 2 system.

Reference:
  https://docs.ros.org/en/jazzy/Tutorials/Advanced/Security/Introducing-ros2-security.html
  https://fast-dds.docs.eprosima.com/en/latest/fastdds/security/security.html
"""

