def setup_security(
    env: EnvironmentInfo,
    keystore: Optional[Path] = None,
    strategy: str = "Enforce",
    enclaves: Optional[list[str]] = None,
    auto_apply: bool = False,
) -> bool:
    """
    Configure SROS2 / DDS-Security for ROS 2.

    Steps:
      1. Create keystore (if it doesn't exist) via ros2 security CLI
      2. Write governance.xml and sign it
      3. Write Fast DDS security XML profile
      4. Create any requested enclaves (nodes)
      5. Write per-enclave permissions.xml files
      6. Generate shell setup scripts

    Parameters
    ----------
    env       : detected environment
    keystore  : path for the keystore (default: ~/.ros2/keystore)
    strategy  : Enforce | Permissive  (default: Enforce)
    enclaves  : list of enclave paths to create, e.g. ["/talker", "/listener"]
                Pass None or [] to skip enclave creation
    auto_apply: skip interactive prompts
    """
    ui.section("SROS2 / DDS-Security Setup")

    if keystore is None:
        keystore = DEFAULT_KEYSTORE

    if strategy not in ("Enforce", "Permissive"):
        ui.warn(f"Unknown strategy '{strategy}' — defaulting to Enforce.")
        strategy = "Enforce"

    ui.info(f"Keystore path:  {keystore}")
    ui.info(f"Strategy:       {strategy} — {STRATEGIES[strategy]}")
    ui.nl()

    # ── Prerequisite check ────────────────────────────────────────────────
    has_ros2_security = _sros2_available()
    if not has_ros2_security:
        ui.warn("ros2 security CLI not found — ROS 2 may not be sourced.")
        ui.warn("Generating config files anyway; keystore creation will be skipped.")
        ui.info("Run 'source /opt/ros/<distro>/setup.bash' then re-run this command.")

    # ── Step 1: Keystore ─────────────────────────────────────────────────
    ui.step("Keystore")
    keystore_ok = False
    if keystore.exists():
        ui.ok(f"Keystore already exists: {keystore}")
        keystore_ok = True
    elif has_ros2_security:
        keystore_ok = _create_keystore(keystore)
    else:
        keystore.mkdir(parents=True, exist_ok=True)
        (keystore / "public").mkdir(exist_ok=True)
        (keystore / "private").mkdir(exist_ok=True)
        ui.warn(f"Created empty keystore directory: {keystore}")
        ui.info("Populate it with: ros2 security create_keystore " + str(keystore))

    # ── Step 2: Governance ────────────────────────────────────────────────
    ui.step("Governance document")
    _sign_governance(keystore)

    # ── Step 3: Fast DDS security XML profile ────────────────────────────
    ui.step("Fast DDS security XML profile")
    CONFIG_DIR.mkdir(parents=True, exist_ok=True)
    sec_xml_path = CONFIG_DIR / "fastdds_security.xml"
    sec_xml_path.write_text(_fastdds_security_xml(keystore))
    ui.ok(f"Security profile written: {sec_xml_path}")

    # ── Step 4 & 5: Enclaves + permissions ───────────────────────────────
    if enclaves:
        ui.step(f"Creating {len(enclaves)} enclave(s)")
        for enclave in enclaves:
            if not enclave.startswith("/"):
                enclave = "/" + enclave
            if has_ros2_security and keystore_ok:
                _create_enclave(keystore, enclave)
            # Write a permissions.xml even if ros2 CLI unavailable
            enc_dir = keystore / enclave.lstrip("/")
            enc_dir.mkdir(parents=True, exist_ok=True)
            perm_xml = enc_dir / "permissions.xml"
            perm_xml.write_text(_permissions_xml(enclave, ["*"], ["*"]))
            ui.ok(f"  Permissions XML: {perm_xml}")
            ui.detail("  Topic wildcards allow all topics — restrict in production.")
    else:
        ui.info("No enclaves specified. Add nodes with:")
        ui.cmd_block(f"ros2 security create_enclave {keystore} /your_node_name")

    # ── Step 6: Shell scripts ─────────────────────────────────────────────
    ui.step("Shell setup scripts")
    scripts = _security_env_scripts(keystore, strategy)
    for filename, content in scripts.items():
        (CONFIG_DIR / filename).write_text(content)
    ui.ok(f"Setup scripts written to: {CONFIG_DIR}")

    # ── Instructions ──────────────────────────────────────────────────────
    _print_activation_instructions(keystore, strategy, sec_xml_path)

    return True


def _print_activation_instructions(
    keystore: Path, strategy: str, sec_xml_path: Path
) -> None:
    ui.section("How to Enable Security")

    ui.info("Source the security setup script in every terminal before running nodes:")
    ui.nl()
    ui.detail("Bash / Zsh:")
    ui.cmd_block(f"source {CONFIG_DIR / 'setup_security.bash'}")
    ui.detail("Fish:")
    ui.cmd_block(f"source {CONFIG_DIR / 'setup_security.fish'}")
    ui.detail("Windows CMD:")
    ui.cmd_block(str(CONFIG_DIR / "setup_security.bat"))
    ui.detail("Windows PowerShell:")
    ui.cmd_block(f". '{CONFIG_DIR / 'setup_security.ps1'}'")

    ui.section("Manual Environment Variables")
    ui.code_block([
        f"ROS_SECURITY_ENABLE=true",
        f"ROS_SECURITY_STRATEGY={strategy}",
        f"ROS_SECURITY_KEYSTORE={keystore}",
        f"FASTRTPS_DEFAULT_PROFILES_FILE={sec_xml_path}",
    ], label="env vars")

    ui.section("Adding Nodes to the Keystore")
    ui.info("Each ROS 2 node needs its own enclave entry. Example:")
    ui.cmd_block(f"ros2 security create_enclave {keystore} /my_robot/sensor_node")
    ui.cmd_block(f"ros2 security create_enclave {keystore} /my_robot/controller_node")

    if strategy == "Enforce":
        ui.warn("Strategy is ENFORCE — nodes without a keystore entry will be REFUSED.")
        ui.info("Switch to Permissive during development: edit setup_security.bash")
        ui.detail("ROS_SECURITY_STRATEGY=Permissive  ← allows unsigned nodes but logs them")

    ui.section("Verifying Security is Active")
    ui.info("After sourcing the script and launching nodes, verify with:")
    ui.cmd_block("ros2 security list_enclaves " + str(keystore))
    ui.cmd_block("ros2 node list  # nodes should appear if certs are valid")

    ui.section("Security Model Summary")
    ui.detail("Authentication : X.509 PKI — nodes prove identity with signed certificates")
    ui.detail("Access Control : permissions.xml — per-node topic allow/deny lists")
    ui.detail("Encryption     : AES-128-GCM   — all RTPS traffic encrypted end-to-end")
    ui.detail("Scope          : All topics (*) allowed by default — restrict for production")


def print_security_status() -> None:
    """Print current SROS2 environment variable status."""
    ui.section("Security Status")
    enabled  = os.environ.get("ROS_SECURITY_ENABLE",   "false").lower() == "true"
    strategy = os.environ.get("ROS_SECURITY_STRATEGY", "(not set)")
    keystore = os.environ.get("ROS_SECURITY_KEYSTORE",  "(not set)")

    ui.kv("ROS_SECURITY_ENABLE",   "true" if enabled else "false (insecure default)", ok_val=enabled)
    ui.kv("ROS_SECURITY_STRATEGY", strategy,   ok_val=strategy == "Enforce")
    ui.kv("ROS_SECURITY_KEYSTORE", keystore,   ok_val=keystore != "(not set)")

    if not enabled:
        ui.warn("Traffic is unencrypted. Run '--fix security' to enable SROS2.")