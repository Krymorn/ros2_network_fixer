def _fastdds_security_xml(keystore: Path) -> str:
    """
    Fast DDS XML profile that enables all three DDS-Security plugins:
    authentication, access-control, and cryptography.

    The paths embedded here point at the files that SROS2 generates
    inside the keystore for each node.  At runtime these are resolved
    relative to $ROS_SECURITY_KEYSTORE/<enclave_path>.
    """
    return textwrap.dedent(f"""\
        <?xml version="1.0" encoding="UTF-8" ?>
        <!--
            Fast DDS Security Profile
            Generated by ros2_network_fixer.

            Enables DDS-Security authentication, access-control, and
            cryptographic plugins for all participants in this process.

            Activate by setting:
              FASTRTPS_DEFAULT_PROFILES_FILE={CONFIG_DIR / "fastdds_security.xml"}
              ROS_SECURITY_ENABLE=true
              ROS_SECURITY_STRATEGY=Enforce
              ROS_SECURITY_KEYSTORE={keystore}
        -->
        <profiles xmlns="http://www.eprosima.com/XMLSchemas/fastRTPS_Profiles">
          <transport_descriptors>
            <!-- Require TLS for all network traffic when using TCP transport.
                 For UDP (default RTPS), DDS-Security handles encryption at
                 the RTPS layer — no transport-level change is needed. -->
          </transport_descriptors>

          <participant profile_name="secure_participant" is_default_profile="true">
            <rtps>
              <propertiesPolicy>
                <properties>

                  <!-- ── Authentication plugin ───────────────────────────── -->
                  <!-- Verifies node identity using X.509 certificates.       -->
                  <property>
                    <name>dds.sec.auth.plugin</name>
                    <value>builtin.PKI-DH</value>
                  </property>
                  <property>
                    <name>dds.sec.auth.builtin.PKI-DH.identity_ca</name>
                    <value>file:{keystore}/public/ca.cert.pem</value>
                  </property>
                  <property>
                    <name>dds.sec.auth.builtin.PKI-DH.identity_certificate</name>
                    <value>file:$(ROS_SECURITY_KEYSTORE)/$(ROS_SECURITY_ENCLAVE_OVERRIDE)/cert.pem</value>
                  </property>
                  <property>
                    <name>dds.sec.auth.builtin.PKI-DH.private_key</name>
                    <value>file:$(ROS_SECURITY_KEYSTORE)/$(ROS_SECURITY_ENCLAVE_OVERRIDE)/key.pem</value>
                  </property>
                  <property>
                    <name>dds.sec.auth.builtin.PKI-DH.password</name>
                    <value></value>
                  </property>

                  <!-- ── Access Control plugin ───────────────────────────── -->
                  <!-- Enforces topic-level publish/subscribe permissions.    -->
                  <property>
                    <name>dds.sec.access.plugin</name>
                    <value>builtin.Access-Permissions</value>
                  </property>
                  <property>
                    <name>dds.sec.access.builtin.Access-Permissions.permissions_ca</name>
                    <value>file:{keystore}/public/ca.cert.pem</value>
                  </property>
                  <property>
                    <name>dds.sec.access.builtin.Access-Permissions.governance</name>
                    <value>file:{keystore}/public/governance.p7s</value>
                  </property>
                  <property>
                    <name>dds.sec.access.builtin.Access-Permissions.permissions</name>
                    <value>file:$(ROS_SECURITY_KEYSTORE)/$(ROS_SECURITY_ENCLAVE_OVERRIDE)/permissions.p7s</value>
                  </property>

                  <!-- ── Cryptographic plugin ────────────────────────────── -->
                  <!-- Encrypts RTPS submessages and payloads (AES-128-GCM). -->
                  <property>
                    <name>dds.sec.crypto.plugin</name>
                    <value>builtin.AES-GCM-GMAC</value>
                  </property>

                  <!-- Enable rtps-level protection (whole RTPS message) -->
                  <property>
                    <name>rtps.participant.rtps_protection_kind</name>
                    <value>ENCRYPT</value>
                  </property>

                </properties>
              </propertiesPolicy>
            </rtps>
          </participant>
        </profiles>
    """)


def _governance_xml() -> str:
    """
    Governance document: domain-wide security rules.

    Sets:
      - allow_unauthenticated_participants = FALSE  (no unsigned nodes)
      - enable_join_access_control = TRUE           (enforce permissions)
      - rtps_protection_kind = ENCRYPT              (encrypt discovery traffic)
      - metadata_protection_kind = ENCRYPT          (encrypt DDS metadata)
      - data_protection_kind = ENCRYPT              (encrypt user data)
    """
    return textwrap.dedent("""\
        <?xml version="1.0" encoding="UTF-8"?>
        <!--
            DDS-Security Governance Document
            Generated by ros2_network_fixer.

            Domain-wide security policy. Signed by the CA and placed at:
              <keystore>/public/governance.p7s  (signed)
              <keystore>/public/governance.xml  (unsigned source)
        -->
        <dds xmlns:xsi="http://www.w3.org/2001/XMLSchema-instance"
             xsi:noNamespaceSchemaLocation="http://www.omg.org/spec/DDS-SECURITY/20170901/omg_shared_ca_governance.xsd">
          <domain_access_rules>
            <domain_rule>
              <!-- Apply to all DDS domains -->
              <domains>
                <id_range>
                  <min>0</min>
                  <max>232</max>
                </id_range>
              </domains>

              <!-- Reject any participant that cannot authenticate -->
              <allow_unauthenticated_participants>false</allow_unauthenticated_participants>

              <!-- Enforce the per-node permissions documents -->
              <enable_join_access_control>true</enable_join_access_control>

              <!-- Encrypt RTPS discovery traffic -->
              <rtps_protection_kind>ENCRYPT</rtps_protection_kind>

              <topic_access_rules>
                <!-- Default rule: encrypt everything -->
                <topic_rule>
                  <topic_expression>*</topic_expression>
                  <enable_discovery_protection>true</enable_discovery_protection>
                  <enable_liveliness_protection>true</enable_liveliness_protection>
                  <enable_read_access_control>true</enable_read_access_control>
                  <enable_write_access_control>true</enable_write_access_control>
                  <metadata_protection_kind>ENCRYPT</metadata_protection_kind>
                  <data_protection_kind>ENCRYPT</data_protection_kind>
                </topic_rule>
              </topic_access_rules>

            </domain_rule>
          </domain_access_rules>
        </dds>
    """)


def _permissions_xml(enclave: str, topics_publish: list[str], topics_subscribe: list[str]) -> str:
    """
    Per-node (enclave) permissions document.

    Grants the named enclave permission to publish and subscribe to
    the specified topic lists within any DDS domain.

    'enclave' should be the ROS 2 enclave path, e.g. '/talker_node'.
    Pass ['*'] for topics_publish / topics_subscribe to allow all topics
    (appropriate for dev/testing; restrict in production).
    """
    pub_allow = "\n".join(
        f"              <topics><topic>{t}</topic></topics>" for t in topics_publish
    )
    sub_allow = "\n".join(
        f"              <topics><topic>{t}</topic></topics>" for t in topics_subscribe
    )

    return textwrap.dedent(f"""\
        <?xml version="1.0" encoding="UTF-8"?>
        <!--
            DDS-Security Permissions Document for enclave: {enclave}
            Generated by ros2_network_fixer.

            Signed and placed at:
              <keystore>{enclave}/permissions.p7s  (signed)
              <keystore>{enclave}/permissions.xml  (unsigned source)
        -->
        <dds xmlns:xsi="http://www.w3.org/2001/XMLSchema-instance"
             xsi:noNamespaceSchemaLocation="http://www.omg.org/spec/DDS-SECURITY/20170901/omg_shared_ca_permissions.xsd">
          <permissions>
            <grant name="{enclave.strip('/')}_grant">

              <!-- Subject name must match the CN in the node's certificate -->
              <subject_name>/C=US/ST=CA/O=ros2_network_fixer/CN={enclave.strip('/')}</subject_name>

              <validity>
                <!-- Valid from now; adjust for production deployments -->
                <not_before>2024-01-01T00:00:00</not_before>
                <not_after>2034-01-01T00:00:00</not_after>
              </validity>

              <allow_rule>
                <domains><id_range><min>0</min><max>232</max></id_range></domains>

                <publish>
{pub_allow}
                </publish>

                <subscribe>
{sub_allow}
                </subscribe>

              </allow_rule>

              <!-- Deny everything not explicitly allowed above -->
              <default>DENY</default>

            </grant>
          </permissions>
        </dds>
    """)


