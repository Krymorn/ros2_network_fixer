def _find_fastdds_server(env: EnvironmentInfo) -> Optional[str]:
    """Locate the fastdds or fastdds-discovery-server binary."""
    candidates = [
        "fastdds",
        "fastdds-discovery-server",
        "discovery-server",
        "fastrtps-discovery-server",
    ]
    for name in candidates:
        path = shutil.which(name)
        if path:
            return path

    # Check ROS 2 install dirs
    if env.ros2_home:
        for subdir in ["lib/fastrtps", "lib/fastdds", "bin"]:
            candidate = env.ros2_home / subdir / "fastdds"
            if candidate.exists():
                return str(candidate)

    # Check common ROS 2 paths
    for distro_dir in Path("/opt/ros").glob("*/lib/fastrtps/fastdds"):
        return str(distro_dir)

    return None


def is_port_free(port: int) -> bool:
    try:
        s = socket.socket(socket.AF_INET, socket.SOCK_DGRAM)
        s.bind(("0.0.0.0", port))
        s.close()
        return True
    except OSError:
        return False


