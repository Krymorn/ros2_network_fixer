# Config generation
# ---------------------------------------------------------------------------

def _discovery_server_xml(server_ip: str, server_port: int) -> str:
    """Generate Fast DDS XML profile for client mode pointing at a server."""
    return textwrap.dedent(f"""\
        <?xml version="1.0" encoding="UTF-8" ?>
        <!--
            Fast DDS Discovery Server client profile.
            Generated by ros2_network_fixer.
            Source this config by setting:
              FASTRTPS_DEFAULT_PROFILES_FILE={CONFIG_DIR / "fastdds_client.xml"}
              ROS_DISCOVERY_SERVER={server_ip}:{server_port}
        -->
        <profiles xmlns="http://www.eprosima.com/XMLSchemas/fastRTPS_Profiles">
          <participant profile_name="discovery_server_client" is_default_profile="true">
            <rtps>
              <builtin>
                <discovery_config>
                  <discoveryProtocol>CLIENT</discoveryProtocol>
                  <discoveryServersList>
                    <RemoteServer prefix="44.53.00.5f.45.50.52.4f.53.49.4d.41">
                      <metatrafficUnicastLocatorList>
                        <locator>
                          <udpv4>
                            <address>{server_ip}</address>
                            <port>{server_port}</port>
                          </udpv4>
                        </locator>
                      </metatrafficUnicastLocatorList>
                    </RemoteServer>
                  </discoveryServersList>
                </discovery_config>
              </builtin>
            </rtps>
          </participant>
        </profiles>
    """)


def _discovery_server_profile_xml(server_port: int) -> str:
    """Generate Fast DDS XML profile for the server itself."""
    return textwrap.dedent(f"""\
        <?xml version="1.0" encoding="UTF-8" ?>
        <!--
            Fast DDS Discovery Server profile.
            Generated by ros2_network_fixer.
        -->
        <profiles xmlns="http://www.eprosima.com/XMLSchemas/fastRTPS_Profiles">
          <participant profile_name="discovery_server" is_default_profile="true">
            <rtps>
              <prefix>44.53.00.5f.45.50.52.4f.53.49.4d.41</prefix>
              <builtin>
                <discovery_config>
                  <discoveryProtocol>SERVER</discoveryProtocol>
                  <metatrafficUnicastLocatorList>
                    <locator>
                      <udpv4>
                        <address>0.0.0.0</address>
                        <port>{server_port}</port>
                      </udpv4>
                    </locator>
                  </metatrafficUnicastLocatorList>
                </discovery_config>
              </builtin>
            </rtps>
          </participant>
        </profiles>
    """)


def _env_export_script(
    server_ip: str,
    server_port: int,
    xml_path: Path,
    os_type: str,
) -> dict[str, str]:
    """Return dict mapping filename → script content for each shell type."""
    env_vars = [
        f'ROS_DISCOVERY_SERVER="{server_ip}:{server_port}"',
        f'FASTRTPS_DEFAULT_PROFILES_FILE="{xml_path}"',
    ]

    bash_lines = [f"export {v}" for v in env_vars]
    fish_lines = [
        f'set -x {v.split("=")[0]} {v.split("=", 1)[1].strip(chr(34))}'
        for v in env_vars
    ]
    win_lines = [
        f'set {v.replace(chr(34), "")}' for v in env_vars
    ]
    ps_lines = [
        f'$env:{v.split("=")[0]} = {v.split("=", 1)[1]}'
        for v in env_vars
    ]

    scripts: dict[str, str] = {}

    scripts["setup_discovery.bash"] = (
        "#!/usr/bin/env bash\n"
        "# Source this file: source setup_discovery.bash\n"
        + "\n".join(bash_lines)
        + "\necho '[ros2_network_fixer] Discovery Server environment set.'\n"
    )

    scripts["setup_discovery.fish"] = (
        "#!/usr/bin/env fish\n"
        "# Source this file: source setup_discovery.fish\n"
        + "\n".join(fish_lines)
        + "\necho '[ros2_network_fixer] Discovery Server environment set.'\n"
    )

    scripts["setup_discovery.bat"] = (
        "@echo off\n"
        "REM Run this file: setup_discovery.bat\n"
        + "\n".join(win_lines)
        + "\necho [ros2_network_fixer] Discovery Server environment set.\n"
    )

    scripts["setup_discovery.ps1"] = (
        "# Run: . .\\setup_discovery.ps1\n"
        + "\n".join(ps_lines)
        + "\nWrite-Host '[ros2_network_fixer] Discovery Server environment set.'\n"
    )

    return scripts


